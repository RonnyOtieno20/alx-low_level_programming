#include <stdio.h>
/**
 * simple calculator to add, subtract, divide and multiply random numbers
 * @a: first integer
 * @b: second integer
 *
 * Return: the result
*/
int add(int a, int b)
{
	return (a + b);
}
int sub(int a, int b)
{
	return (a - b);
}
int mul(int a, int b)
{
	return (a * b);
}
int div(int a, int b)
{
	return (a / b);
}
int mod(int a, int b)
{
	return (a % b);
}
